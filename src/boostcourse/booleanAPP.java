package boostcourse;

public class booleanAPP {
    public static void main(String[] args) {
        System.out.println(true);
        System.out.println(false);
        String foo = "hello word!";
        // 예약어  : reserved word , 여기서는 true와 false가 예약어 이다.
        System.out.println(foo.contains("hello")); //들어가있어서 true이다
        System.out.println(foo.contains("hi"));
    }
}
